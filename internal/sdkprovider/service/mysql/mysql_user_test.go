func TestAccAivenMySQLUser_basic(t *testing.T) {
	resourceName := "aiven_mysql_user.foo"
	rName := acctest.RandStringFromCharSet(10, acctest.CharSetAlphaNum)

	resource.ParallelTest(t, resource.TestCase{
		PreCheck:                 func() { acc.TestAccPreCheck(t) },
		ProtoV6ProviderFactories: acc.TestProtoV6ProviderFactories,
		CheckDestroy:             testAccCheckAivenMySQLUserResourceDestroy,
		Steps: []resource.TestStep{
			{
				// create with optional password
				Config: testAccMySQLUserResource(rName, "Test$1234"),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - using optional password
					resource.TestCheckResourceAttr(resourceName, "password", "Test$1234"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo_version"),

					// Data source
					schemautil.TestAccCheckAivenServiceUserAttributes("data.aiven_mysql_user.user"),
				),
			},
			{
				// update optional password
				Config: testAccMySQLUserResource(rName, "UpdatedP@ss5678"),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - updated optional password
					resource.TestCheckResourceAttr(resourceName, "password", "UpdatedP@ss5678"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo_version"),

					// Data source
					schemautil.TestAccCheckAivenServiceUserAttributes("data.aiven_mysql_user.user"),
				),
			},
			{
				// transition to auto-generated password
				Config: testAccMySQLUserAutoGeneratedPassword(rName),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - auto-generated
					resource.TestCheckResourceAttrSet(resourceName, "password"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo_version"),
				),
			},
			{
				// back to optional password
				Config: testAccMySQLUserResource(rName, "Optional$Pass123"),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - back to optional
					resource.TestCheckResourceAttr(resourceName, "password", "Optional$Pass123"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo_version"),
				),
			},
			{
				// transition to write-only password
				Config: testAccMySQLUserWriteOnlyPassword(rName, 1),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - using write-only password (password cleared, version set)
					resource.TestCheckResourceAttr(resourceName, "password", ""),
					resource.TestCheckResourceAttr(resourceName, "password_wo_version", "1"),
				),
			},
			{
				// rotate write-only password
				Config: testAccMySQLUserWriteOnlyPassword(rName, 2),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - rotated write-only password (password cleared, version incremented)
					resource.TestCheckResourceAttr(resourceName, "password", ""),
					resource.TestCheckResourceAttr(resourceName, "password_wo_version", "2"),
				),
			},
			{
				// transition back to auto-generated password
				Config: testAccMySQLUserAutoGeneratedPassword(rName),
				Check: resource.ComposeTestCheckFunc(
					// Basic attributes
					resource.TestCheckResourceAttr(resourceName, "service_name", fmt.Sprintf("test-acc-sr-%s", rName)),
					resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
					resource.TestCheckResourceAttr(resourceName, "username", fmt.Sprintf("user-%s", rName)),

					// Password - back to auto-generated (password set, version reset to 0)
					resource.TestCheckResourceAttrSet(resourceName, "password"),
					resource.TestCheckNoResourceAttr(resourceName, "password_wo"),
					resource.TestCheckResourceAttr(resourceName, "password_wo_version", "0"),
				),
			},
		},
	})
}

func testAccCheckAivenMySQLUserResourceDestroy(s *terraform.State) error {
	c, err := acc.GetTestGenAivenClient()
	if err != nil {
		return fmt.Errorf("error instantiating client: %w", err)
	}

	ctx := context.Background()

	// loop through the resources in state, verifying each aiven_mysql_user is destroyed
	for _, rs := range s.RootModule().Resources {
		if rs.Type != "aiven_mysql_user" {
			continue
		}

		projectName, serviceName, username, err := schemautil.SplitResourceID3(rs.Primary.ID)
		if err != nil {
			return err
		}

		_, err = c.ServiceUserGet(ctx, projectName, serviceName, username)
		if err != nil && !avngen.IsNotFound(err) {
			return fmt.Errorf("error checking if user was destroyed: %w", err)
		}

		if err == nil {
			return fmt.Errorf("mysql user (%s) still exists", rs.Primary.ID)
		}
	}

	return nil
}

