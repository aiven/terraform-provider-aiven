func TestAccAivenOpenSearchUser_basic(t *testing.T) {
	resourceName := "aiven_opensearch_user.foo"
	projectName := acc.ProjectName()
	serviceName := acc.RandName("opensearch")
	serviceIsReady := acc.CreateTestService(
		t,
		projectName,
		serviceName,
		acc.WithServiceType("opensearch"),
		acc.WithPlan("startup-4"),
		acc.WithCloud("google-europe-west1"),
	)

	t.Run("basic test", func(t *testing.T) {
		userName := acc.RandName("user")
		resource.ParallelTest(t, resource.TestCase{
			PreCheck: func() {
				acc.TestAccPreCheck(t)
				require.NoError(t, <-serviceIsReady)
			},
			ProtoV6ProviderFactories: acc.TestProtoV6ProviderFactories,
			CheckDestroy:             testAccCheckAivenOpenSearchUserResourceDestroy,
			Steps: []resource.TestStep{
				{
					// create with custom password
					Config: testAccOpenSearchUserResource(projectName, serviceName, userName, "acc-custom-ThisIsATest123^=^"),
					Check: resource.ComposeTestCheckFunc(
						schemautil.TestAccCheckAivenServiceUserAttributes("data.aiven_opensearch_user.user"),
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasCustomPassword(resourceName, "acc-custom-ThisIsATest123^=^"),
					),
				},
				{
					// update custom password
					Config: testAccOpenSearchUserResource(projectName, serviceName, userName, "acc-custom-UpdatedPass$123"),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasCustomPassword(resourceName, "acc-custom-UpdatedPass$123"),
					),
				},
				{
					// transition to auto-generated password
					Config: testAccOpenSearchUserAutoGenerated(projectName, serviceName, userName),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasGeneratedPassword(resourceName),
					),
				},
				{
					// back to custom password
					Config: testAccOpenSearchUserResource(projectName, serviceName, userName, "acc-custom-Optional$Pass123"),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasCustomPassword(resourceName, "acc-custom-Optional$Pass123"),
					),
				},
				{
					// transition to write-only password
					Config: testAccOpenSearchUserWriteOnly(projectName, serviceName, userName, 1),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasWOPassword(resourceName),
					),
				},
				{
					// rotate write-only password
					Config: testAccOpenSearchUserWriteOnly(projectName, serviceName, userName, 2),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasWOPassword(resourceName),
					),
				},
				{
					// transition back to auto-generated password
					Config: testAccOpenSearchUserAutoGenerated(projectName, serviceName, userName),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasGeneratedPassword(resourceName),
					),
				},
				{
					// final custom password
					Config: testAccOpenSearchUserResource(projectName, serviceName, userName, "acc-custom-FinalPass$456"),
					Check: resource.ComposeTestCheckFunc(
						resource.TestCheckResourceAttr(resourceName, "service_name", serviceName),
						resource.TestCheckResourceAttr(resourceName, "project", acc.ProjectName()),
						resource.TestCheckResourceAttr(resourceName, "username", userName),
						acc.TestAccPasswordHasCustomPassword(resourceName, "acc-custom-FinalPass$456"),
					),
				},
			},
		})
	})

	// Compatibility test
	t.Run("backward compatibility test", func(t *testing.T) {
		var (
			userName        = acc.RandName("user")
			providerVersion = "4.47.0"
		)

		resource.ParallelTest(t, resource.TestCase{
			PreCheck: func() {
				acc.TestAccPreCheck(t)
				require.NoError(t, <-serviceIsReady)
			},
			Steps: acc.BackwardCompatibilitySteps(t, acc.BackwardCompatConfig{
				TFConfig: fmt.Sprintf(`
resource "aiven_opensearch_user" "test" {
  project      = %[1]q
  service_name = %[2]q
  username     = %[3]q
}

data "aiven_opensearch_user" "test" {
  project      = aiven_opensearch_user.test.project
  service_name = aiven_opensearch_user.test.service_name
  username     = aiven_opensearch_user.test.username
}`, projectName, serviceName, userName),
				OldProviderVersion: providerVersion,
				Checks: resource.ComposeTestCheckFunc(
					resource.TestCheckResourceAttrSet("aiven_opensearch_user.test", "id"),
					resource.TestCheckResourceAttr("aiven_opensearch_user.test", "username", userName),
					resource.TestCheckResourceAttrSet("aiven_opensearch_user.test", "password"),
					resource.TestCheckResourceAttr("aiven_opensearch_user.test", "type", "normal"),

					resource.TestCheckResourceAttr("data.aiven_opensearch_user.test", "username", userName),
					resource.TestCheckResourceAttrSet("data.aiven_opensearch_user.test", "password"),
				),
			}),
		})
	})
}

func testAccCheckAivenOpenSearchUserResourceDestroy(s *terraform.State) error {
	c, err := acc.GetTestGenAivenClient()
	if err != nil {
		return fmt.Errorf("error instantiating client: %w", err)
	}

	ctx := context.Background()

	// loop through the resources in state, verifying each aiven_opensearch_user is destroyed
	for _, rs := range s.RootModule().Resources {
		if rs.Type != "aiven_opensearch_user" {
			continue
		}

		projectName, serviceName, username, err := schemautil.SplitResourceID3(rs.Primary.ID)
		if err != nil {
			return err
		}

		_, err = c.ServiceUserGet(ctx, projectName, serviceName, username)
		if err != nil && !avngen.IsNotFound(err) {
			return fmt.Errorf("error checking if user was destroyed: %w", err)
		}

		if err == nil {
			return fmt.Errorf("opensearch user (%s) still exists", rs.Primary.ID)
		}
	}

	return nil
}

