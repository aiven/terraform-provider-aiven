package user

import (
	"context"
	"fmt"
	"net/http"

	avngen "github.com/aiven/go-client-codegen"
	"github.com/aiven/go-client-codegen/handler/clickhouse"

	"github.com/aiven/terraform-provider-aiven/internal/plugin/adapter"
	"github.com/aiven/terraform-provider-aiven/internal/plugin/util"
)

func init() {
	ResourceOptions.Update = updateView
}

func createView(ctx context.Context, client avngen.Client, d adapter.ResourceData) error {
	project := d.Get("project").(string)
	serviceName := d.Get("service_name").(string)
	username := d.Get("username").(string)

	user, err := client.ServiceClickHouseUserCreate(ctx, project, serviceName, &clickhouse.ServiceClickHouseUserCreateIn{
		Name: username,
	})
	if err != nil {
		return err
	}

	if err = resetPassword(ctx, client, d, user.Uuid, user.Password); err != nil {
		return err
	}

	if err = d.Set("uuid", user.Uuid); err != nil {
		return err
	}

	return d.SetID(project, serviceName, user.Uuid)
}

func updateView(ctx context.Context, client avngen.Client, d adapter.ResourceData) error {
	return resetPassword(ctx, client, d, d.Get("uuid").(string), nil)
}

func resetPassword(
	ctx context.Context,
	client avngen.Client,
	d adapter.ResourceData,
	uuid string,
	autoGeneratedPassword *string,
) error {
	password := util.NilIfZero(d.Get("password").(string), d.Get("password_wo").(string))
	if d.IsNewResource() && password == nil && autoGeneratedPassword != nil {
		return d.Set("password", *autoGeneratedPassword)
	}

	if !d.HasChange("password") && !d.HasChange("password_wo_version") {
		return nil
	}

	newPassword, err := client.ServiceClickHousePasswordReset(
		ctx,
		d.Get("project").(string),
		d.Get("service_name").(string),
		uuid,
		&clickhouse.ServiceClickHousePasswordResetIn{
			Password: password,
		},
	)
	if err != nil {
		return err
	}

	if d.Get("password_wo_version").(int) != 0 {
		return d.Set("password", nil)
	}

	if password == nil {
		return d.Set("password", newPassword)
	}

	return nil
}

func planModifier(ctx context.Context, client avngen.Client, d adapter.ResourceData) error {
	if d.Get("uuid").(string) != "" {
		return nil
	}

	project := d.Get("project").(string)
	serviceName := d.Get("service_name").(string)
	username := d.Get("username").(string)

	users, err := client.ServiceClickHouseUserList(ctx, project, serviceName)
	if err != nil {
		return err
	}

	for _, user := range users {
		if user.Name == username {
			if err = d.Set("uuid", user.Uuid); err != nil {
				return err
			}

			return d.SetID(project, serviceName, user.Uuid)
		}
	}

	return avngen.Error{
		Message:     fmt.Sprintf("clickhouse user %q not found in project %q, service %q", username, project, serviceName),
		OperationID: "ServiceClickHouseUserList",
		Status:      http.StatusNotFound,
	}
}

func flattenModifier(_ context.Context, _ avngen.Client) adapter.MapModifier {
	return func(d adapter.ResourceData, dto map[string]any) error {
		if _, ok := d.Schema().Properties["password_wo_version"]; ok && d.Get("password_wo_version").(int) != 0 {
			delete(dto, "password")
			return nil
		}

		if password := d.Get("password").(string); password != "" {
			dto["password"] = password
		}

		return nil
	}
}
